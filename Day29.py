# Docstrings

# docstrings are NOT comment & they are not ignored by the interpreter 

# Python DOCSTRINGS are the string literals that appear right after the defintion of an Function, class or Module 

def cube(n):
    '''This function takes a input N and returns the cube of it'''
    print(n**3)

cube(5)
print(cube.__doc__)

